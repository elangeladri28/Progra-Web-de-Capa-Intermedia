var Curso = function(id_curso, nombre, descripcion, costo, foto, video, categoriaid, activo, fechaCreado, usuid) {

    this.id_curso = id_curso;
    this.nombre = nombre;
    this.descripcion = descripcion;
    this.costo = costo;
    this.foto = foto;
    this.video = video;
    this.categoriaid = categoriaid;
    this.activo = activo;
    this.fechaCreado = fechaCreado;
    this.usuid = usuid;
};